int	fib(int i, int a, int b)
{
	if (i == 0)
		return (a);
	return (fib(i - 1, b, a + b));
}

int	ft_fibonacci(int index)
{
	if (index < 0)
		return (-1);
	return (fib(index, 0, 1));
}
