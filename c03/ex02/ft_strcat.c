char	*ft_strcat(char *dest, char *src)
{
	int	m;
	int	s;

	m = 0;
	s = 0;
	while (dest[m] != '\0')
		m++;
	while (src[s] != '\0')
	{
		dest[m] = src[s];
		m++;
		s++;
	}
	dest[m] = '\0';
	return (dest);
}
