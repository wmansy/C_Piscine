char	*ft_strstr(char *str, char *to_find)
{
	int	j;

	if (to_find[0] == '\0')
		return (str);
	while (*str)
	{
		j = 0;
		while (str[j] == to_find[j])
		{
			j++;
			if (to_find[j] == '\0')
				return (str);
		}
		str++;
	}
	return (0);
}
