int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int		i;
	int		q;

	i = 0;
	q = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			q++;
		i++;
	}
	return (q);
}
