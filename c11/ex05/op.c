#include "do_op.h"

long	ft_addition(long nb1, long nb2)
{
	return (nb1 + nb2);
}

long	ft_substraction(long nb1, long nb2)
{
	return (nb1 - nb2);
}

long	ft_multiplication(long nb1, long nb2)
{
	return (nb1 * nb2);
}

long	ft_division(long nb1, long nb2)
{
	return (nb1 / nb2);
}

long	ft_modulo(long nb1, long nb2)
{
	return (nb1 % nb2);
}
